class Qwin_Bind extends Qwin_Widget
{
    /**
     * 绑定事件
     * 
     * @see Qwin_Event::add()
     * @param string $event 事件名称
     * @param mixed $callback 回调结构
     * @return void
     */
    public function call($event, $callback, $priority = 10)
    {
        return $this->event->add($event, $callback, $priority);
    }
}
