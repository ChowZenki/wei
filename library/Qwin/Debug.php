class Qwin_Debug
{
    function p($a)
    {
        print_r($a);
    }
    
    function e($data = '', $is_exit = true)
    {
        if(is_array($data) || is_object($data))
        {
            print_r($data);
        } else {
            echo $data;
        }
        $is_exit == true && exit();
    }
}
